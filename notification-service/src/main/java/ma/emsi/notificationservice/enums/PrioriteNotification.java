package ma.emsi.notificationservice.enums;

/**
 * Enum representing the priority level of a notification.
 */
public enum PrioriteNotification {
    /**
     * Normal priority - standard notifications
     */
    NORMALE,
    
    /**
     * High priority - important notifications that should be processed quickly
     */
    HAUTE,
    
    /**
     * Urgent priority - critical notifications requiring immediate attention
     */
    URGENTE
}
